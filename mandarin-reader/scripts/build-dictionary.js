#!/usr/bin/env node
// One-time/dev-time generator for src/data/cedict.ts.
//
// Downloads CC-CEDICT (traditional/simplified/pinyin/definitions) and jieba's
// corpus word-frequency list, then curates a subset small enough to embed
// directly in the JS bundle (no expo-file-system/expo-asset dependency):
//   - every single-character entry (universal fallback for any tapped hanzi)
//   - the 10,000 highest-frequency multi-character entries (by jieba corpus
//     frequency), since CC-CEDICT itself carries no frequency data and the
//     full dictionary (~125k entries, ~10MB) is too large to bundle as JS.
// Re-run with `node scripts/build-dictionary.js` to regenerate.

const https = require("https");
const zlib = require("zlib");
const fs = require("fs");
const path = require("path");

const CEDICT_URL =
  "https://www.mdbg.net/chinese/export/cedict/cedict_1_0_ts_utf-8_mdbg.txt.gz";
const JIEBA_FREQ_URL =
  "https://raw.githubusercontent.com/fxsjy/jieba/master/jieba/dict.txt";
const MULTI_CHAR_CAP = 10000;
const OUT_FILE = path.join(__dirname, "../src/data/cedict.ts");

function fetch(url) {
  return new Promise((resolve, reject) => {
    https
      .get(url, (res) => {
        if (res.statusCode >= 300 && res.statusCode < 400 && res.headers.location) {
          resolve(fetch(res.headers.location));
          return;
        }
        if (res.statusCode !== 200) {
          reject(new Error(`${url} -> HTTP ${res.statusCode}`));
          return;
        }
        const chunks = [];
        res.on("data", (c) => chunks.push(c));
        res.on("end", () => resolve(Buffer.concat(chunks)));
        res.on("error", reject);
      })
      .on("error", reject);
  });
}

const CEDICT_LINE_RE = /^(\S+) (\S+) \[([^\]]+)\] \/(.+)\/$/;

function parseCedict(text) {
  const entries = [];
  for (const line of text.split(/\r?\n/)) {
    if (!line || line.startsWith("#")) continue;
    const m = line.match(CEDICT_LINE_RE);
    if (!m) continue;
    const [, traditional, simplified, pinyin, defsRaw] = m;
    const definitions = defsRaw.split("/").filter(Boolean);
    const isVariantOnly =
      definitions.length === 1 && /variant of|see also|^see /i.test(definitions[0]);
    if (isVariantOnly) continue;
    entries.push({
      traditional,
      simplified,
      pinyin,
      definitions,
      len: Array.from(simplified).length,
    });
  }
  return entries;
}

function parseJiebaFreq(text) {
  const freq = new Map();
  for (const line of text.split(/\r?\n/)) {
    if (!line) continue;
    const parts = line.split(" ");
    if (parts.length < 2) continue;
    const f = parseInt(parts[1], 10);
    if (!isNaN(f)) freq.set(parts[0], Math.max(freq.get(parts[0]) || 0, f));
  }
  return freq;
}

function toTsvLine(e) {
  return [e.traditional, e.simplified, e.pinyin, e.definitions.join("|")].join("\t");
}

async function main() {
  console.log("Downloading CC-CEDICT...");
  const gz = await fetch(CEDICT_URL);
  const cedictText = zlib.gunzipSync(gz).toString("utf-8");
  const entries = parseCedict(cedictText);
  console.log(`Parsed ${entries.length} CC-CEDICT entries.`);

  console.log("Downloading jieba frequency list...");
  const jiebaText = (await fetch(JIEBA_FREQ_URL)).toString("utf-8");
  const freq = parseJiebaFreq(jiebaText);
  console.log(`Loaded ${freq.size} frequency entries.`);

  const oneChar = entries.filter((e) => e.len === 1);
  const multiChar = entries
    .filter((e) => e.len >= 2)
    .map((e) => ({ ...e, freq: freq.get(e.simplified) || 0 }))
    .filter((e) => e.freq > 0)
    .sort((a, b) => b.freq - a.freq)
    .slice(0, MULTI_CHAR_CAP);

  const curated = [...oneChar, ...multiChar];
  const tsv = curated.map(toTsvLine).join("\n");
  console.log(
    `Curated ${curated.length} entries (${oneChar.length} single-char + ${multiChar.length} multi-char), ${Buffer.byteLength(tsv)} bytes.`
  );

  const header = `// Generated by scripts/build-dictionary.js — do not edit by hand.
// Curated subset of CC-CEDICT (https://www.mdbg.net/chinese/dictionary?page=cc-cedict,
// Creative Commons Attribution-ShareAlike License): every single-character entry
// plus the ${MULTI_CHAR_CAP} highest-frequency multi-character entries, ranked by
// jieba's corpus word-frequency list (https://github.com/fxsjy/jieba, MIT License).
// Format: one entry per line, tab-separated
//   traditional\\tsimplified\\tpinyin\\tdefinition1|definition2|...
`;
  const out = `${header}\nexport const CEDICT_TSV: string = ${JSON.stringify(tsv)};\n`;
  fs.writeFileSync(OUT_FILE, out, "utf-8");
  console.log(`Wrote ${OUT_FILE} (${Buffer.byteLength(out)} bytes).`);
}

main().catch((err) => {
  console.error(err);
  process.exit(1);
});
